    {
        // dd($request->all());
        $this->validate($request, [
            'name' => 'required',
            'email' => 'required|email',
            'subject' => 'required|max:255',
            'message' => 'required|max:2000',
            ]);
           Contact::create($request->all());
           return back()->with('success', 'Thanks for contacting us!');
    }
   

